function initMobileMenu() {
  const header = document.querySelector('header');
  const toggle = document.querySelector('.menu-toggle');
  const links = document.querySelectorAll('header nav a');
  if (!header || !toggle) return;

  function setOpen(isOpen) {
    header.classList.toggle('menu-open', isOpen);
    toggle.setAttribute('aria-expanded', String(isOpen));
    toggle.setAttribute('aria-label', isOpen ? 'Fechar menu' : 'Abrir menu');
  }

  toggle.addEventListener('click', function () {
    setOpen(!header.classList.contains('menu-open'));
  });

  links.forEach(function (link) {
    link.addEventListener('click', function () {
      setOpen(false);
    });
  });

  window.addEventListener('resize', function () {
    if (window.innerWidth > 900) setOpen(false);
  }, { passive: true });
}

function initReveal() {
  const elements = document.querySelectorAll('.reveal');
  if (!elements.length) return;

  const observer = new IntersectionObserver(function (entries) {
    entries.forEach(function (entry) {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.15 });

  elements.forEach(function (el) { observer.observe(el); });
}

function initServiceParticles() {
  const canvas = document.querySelector('.services-particles');
  const section = document.querySelector('.services-section');
  if (!canvas || !section) return;

  const reducedMotion = window.matchMedia('(prefers-reduced-motion: reduce)').matches;
  const ctx = canvas.getContext('2d');
  const mouse = { x: -9999, y: -9999, active: false };
  let particles = [];
  let animationFrame = null;
  let width = 0;
  let height = 0;
  let dpr = 1;

  function resize() {
    const rect = section.getBoundingClientRect();
    dpr = Math.min(window.devicePixelRatio || 1, 2);
    width = Math.max(1, Math.floor(rect.width));
    height = Math.max(1, Math.floor(rect.height));
    canvas.width = Math.floor(width * dpr);
    canvas.height = Math.floor(height * dpr);
    canvas.style.width = width + 'px';
    canvas.style.height = height + 'px';
    ctx.setTransform(dpr, 0, 0, dpr, 0, 0);
    createParticles();
    draw();
  }

  function createParticles() {
    const count = Math.min(46, Math.max(22, Math.floor(width / 34)));
    particles = Array.from({ length: count }, function (_, index) {
      return {
        x: (width / count) * index + Math.random() * 26,
        y: Math.random() * height,
        vx: (Math.random() - 0.5) * 0.18,
        vy: (Math.random() - 0.5) * 0.18,
        size: Math.random() * 1.7 + 1
      };
    });
  }

  function drawLine(a, b, alpha) {
    ctx.strokeStyle = 'rgba(255, 143, 63, ' + alpha + ')';
    ctx.lineWidth = 1;
    ctx.beginPath();
    ctx.moveTo(a.x, a.y);
    ctx.lineTo(b.x, b.y);
    ctx.stroke();
  }

  function draw() {
    ctx.clearRect(0, 0, width, height);

    particles.forEach(function (particle, index) {
      if (!reducedMotion) {
        particle.x += particle.vx;
        particle.y += particle.vy;

        if (particle.x < -20) particle.x = width + 20;
        if (particle.x > width + 20) particle.x = -20;
        if (particle.y < -20) particle.y = height + 20;
        if (particle.y > height + 20) particle.y = -20;

        if (mouse.active) {
          const dx = particle.x - mouse.x;
          const dy = particle.y - mouse.y;
          const distance = Math.sqrt(dx * dx + dy * dy);
          if (distance < 150) {
            const force = (150 - distance) / 150;
            particle.x += (dx / Math.max(distance, 1)) * force * 0.55;
            particle.y += (dy / Math.max(distance, 1)) * force * 0.55;
          }
        }
      }

      for (let j = index + 1; j < particles.length; j += 1) {
        const other = particles[j];
        const dx = particle.x - other.x;
        const dy = particle.y - other.y;
        const distance = Math.sqrt(dx * dx + dy * dy);
        if (distance < 118) {
          drawLine(particle, other, (1 - distance / 118) * 0.18);
        }
      }

      if (mouse.active) {
        const dx = particle.x - mouse.x;
        const dy = particle.y - mouse.y;
        const distance = Math.sqrt(dx * dx + dy * dy);
        if (distance < 170) {
          drawLine(particle, mouse, (1 - distance / 170) * 0.32);
        }
      }

      ctx.fillStyle = 'rgba(255, 255, 255, .42)';
      ctx.beginPath();
      ctx.arc(particle.x, particle.y, particle.size, 0, Math.PI * 2);
      ctx.fill();
    });

    if (!reducedMotion) {
      animationFrame = requestAnimationFrame(draw);
    }
  }

  section.addEventListener('mousemove', function (event) {
    const rect = section.getBoundingClientRect();
    mouse.x = event.clientX - rect.left;
    mouse.y = event.clientY - rect.top;
    mouse.active = true;
  }, { passive: true });

  section.addEventListener('mouseleave', function () {
    mouse.active = false;
  });

  window.addEventListener('resize', resize, { passive: true });
  resize();

  if (reducedMotion && animationFrame) {
    cancelAnimationFrame(animationFrame);
  }
}
